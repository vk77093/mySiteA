<p>As a result of our quest for exquisite chocolates,  we at Aaria,  collaborated with Crea International, Italy, to bring forth premium chocolate & chocolate products that we are proud of.</p>
<p>The process begins with the procurement of the finest cocoa beans from the world’s best production areas where they are hand-picked to ensure that the optimum level of aromatics is present in the ripened pods. The selected cocoa pods are then fermented & dried in the tropical sun, changing the organic compounds in the beans that impart its unique colour & flavour. The beans are then shipped to our state-of-the-art facility site in Cervasca (Cuneo) where they are sorted, nibbed, roasted, ground, mixed & refined. All this is done while employing the most current production & traceability technologies, at the same time ensuring maximum compliance with the most stringent international standards, spontaneous testing & monitoring. The mix of cocoa butter & cocoa mass so obtained is combined with varying proportions of sugar and milk and is stirred continuously over several days in a process called “conching” that gives our chocolate a smooth, silky texture. It is then cooled slowly in the process called “tempering” resulting in the mixture called “couverture” that is used as the basis of most finished products. Other ingredients like the most precious hazelnuts, choicest of dry fruits, natural vanilla, cocoa butter & lecithin are added and the mix is made into premium moulds to give Aaria chocolates the luxurious melt-in-the-mouth feel.</p>
