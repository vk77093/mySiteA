<ul>
    <li class="compDesc">Preheat the oven to 180° C/350° F/gas mark 4. Place two metal baking sheets in the freezer.</li>
    <li class="compDesc">Put the seeds and a dozen chilli slices on to a baking tray and toast in the oven for 5 mins or until the sunflower seeds are just starting to brown. Remove and allow to cool.</li>
