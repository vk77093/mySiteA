        <p class="comp">PREPARATION</p>
<ul>
    <li class="compDesc">Take the ingredients out of the fridge and allow them to soften. Preheat the oven to 180° C/350° F/gas mark 4 and lightly grease a 20cm x 30cm baking tray with butter. Place the raspberries in a bowl and stir in the brandy or kirsch. Set aside.</li>
    <li class="compDesc">Melt Aaria Dark Chocolate – Ecuadaor and butter in a large saucepan over a low heat. When the chocolate is completely melted, remove from the heat, whisk in the milk and cool for about 5 minutes. Stir in the sugar and vanilla. Stir in the eggs one by one. Add the flour, baking powder and salt, and fold in until just combined. Fold in about half the raspberries and spread this brownie batter into the tray.</li>
    <li class="compDesc">Beat the batter with the cream cheese, butter, egg, sugar and almond extract until light and fluffy. Gently fold in the other half of the raspberries and their juices.</li>
    <li class="compDesc">Drop the cheese mixture on top of the brownie batter in spoonfuls, then swirl it through the batter with a knife. Bake for 30 minutes or until just barely set. The top will be just turning light brown. Let it cool for at least 10 minutes before slicing.</li>
    <li class="compDesc">Store at room temperature, well covered. The flavour and texture of these brownies really bloom if you let them rest overnight.</li>

