        <p class="comp">PREPARATION</p>
<ul>
    <li class="compDesc">Put Aaria Dark Chocolate Jamaica (72%), condensed milk, butter and salt in a heavy-based pan on a low heat, and stir to melt.</li>
<li class="compDesc">Put the nuts into a freezer bag and bash them with a rolling pin, until broken up into both big and little pieces.</li>
<li class="compDesc">Add the nuts to the melted chocolate and condensed milk and stir well to mix.</li>
<li class="compDesc">Pour and spread this mixture into a foil tray 23cm / 9 inch square, smoothing the top.</li>
<li class="compDesc">Let the fudge cool, and refrigerate until set. You can then cut it into small pieces approx. 3 x 2.25cm / 1¾ x 1¾ inches. Cutting 8 x 10 / 7 x 7 lines in the tin to give 64 pieces best achieves this.</li>
