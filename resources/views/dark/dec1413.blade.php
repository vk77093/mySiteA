        <p class="comp">PREPARATION</p>
<ul>
    <li class="compDesc">Preheat the oven to 170° C/150° C Fan/gas mark 3/325° F. Melt the 125g  Aaria dark chocolate either in the microwave or in a heatproof dish over a pan of simmering water.</li>
    <li class="compDesc">Put the flour, cocoa, baking soda (bicarbonate soda) and salt into a bowl.</li>
    <li class="compDesc">Cream the butter and sugars in another bowl. You can use a regular hand blender as well for this step.</li>
    <li class="compDesc">Then, add the melted chocolate and mix together again.</li>
    <li class="compDesc">Beat in the vanilla extract and cold egg, and then mix in the dry ingredients. Finally stir in the chocolate morsels or chips.</li>
    <li class="compDesc">Scoop out 12 equal-sized mounds – an ice cream scoop and a palette knife are the best tools for the job – and place on a lined baking sheet about 6cm apart. Do not flatten them.</li>
    <li class="compDesc">Cook for 18 minutes, testing with a cake tester to make sure it comes out semi-clean and not wet with cake batter. If you pierce a chocolate chip, try again.</li>
    <li class="compDesc">Leave to cool slightly on the baking sheet for 4-5 minutes, then transfer them to a cooling rack to harden as they cool.</li>

