        <p class="comp">PREPARATION</p>
<ul>
    <li class="compDesc">Line a standard baking sheet with parchment paper. Set aside.</li>
    <li class="compDesc">Place the  Aaria dark chocolate in a heatproof bowl.</li>
    <li class="compDesc">Set bowl of chocolate over pot of water. Ensure water does not touch the bottom of the bowl.</li>
    <li class="compDesc">Bring the pot of water to a simmer.</li>
    <li class="compDesc">When chocolate is almost completely melted, remove from heat and stir to finish melting.</li>
    <li class="compDesc">Combine the toasted nuts with the melted chocolate.</li>
    <li class="compDesc">Spread out mixture on the parchment paper to about ¼-½ inch thick.</li>
    <li class="compDesc">Place the chocolate bark in the fridge for about 10 minutes, this will help set the chocolate.</li>
    <li class="compDesc">Break or cut into chunks.</li>
</ul>
 <p class="comp">Notes</p>
 <ul>
     <li class="compDesc">Make sure anything that comes into contact with the chocolate is completely dry. The slightest amount of moisture will cause the chocolate to seize and become grainy.</li>
