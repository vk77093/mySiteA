        <p class="comp">PREPARATION</p>
<ul>
    <li class="compDesc">Heat the oven to 170° C/150° C fan/gas 3.5. Put all the cake ingredients in a large mixing bowl. Using an electric hand mixer, beat everything together to make a thick, smooth batter (this will take about 5 minutes).</li>
    <li class="compDesc">Line the muffin tin with paper cases and divide the batter equally among them. Bake for 20-25 minutes until the cake is well risen and a skewer inserted into the center comes out clean.</li>
    <li class="compDesc">For the ganache, put the Aaria Dark Chocolate Madagascar in a microwaveable bowl, cover with cling film then heat on the lowest setting for a minute at a time, stirring after each minute until melted. Let stand for 5 minutes then use a balloon whisk to slowly beat in the cream a little at a time. When a thick glossy ganache forms, use a small palette knife to spread the ganache onto the cupcakes. Chill in the fridge (cover once the ganache has set) or leave at room temperature if you are eating straightaway. They are best eaten at room temp, so take them out of the fridge an hour before eating.</li>
</ul>
<p class="comp">Delicious Tips</p>
<ul>
    <li class="compDesc">Once the cakes are a few days old, slice a piece and warm through in the microwave for a few minutes then serve with cream and seasonal fruit.</li>
    <li class="compDesc">Prepare the iced cake up to 24 hours in advance and store in a sealed container in the fridge. Or freeze the un-iced cake, wrapped in cling film, for up to 1 month in a sealed container. Make, cover and chill the ganache up to 48 hours in advance. Let it come to room temperature before decorating the cake.</li>
</ul>
<p class="comp">Fillings-</p>
<ul>
    <li class="compDesc">Fill a piping bag with the prepared ganache filling and pipe about a tablespoon of the filling onto the first half of the macron shells, then top with a matching by size macron half. Press gently to adhere.</li>

</ul>
